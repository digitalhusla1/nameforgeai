// Test business name generation with keywords
const fetch = require('node-fetch');

async function testBusinessNameGeneration() {
    console.log('🚀 Testing Business Name Generation with Keywords...\n');
    
    const testDescriptions = [
        'eco-friendly coffee shop with sustainable practices',
        'modern tech startup focused on AI and automation',
        'luxury spa and wellness center',
        'organic food delivery service',
        'innovative fitness app for busy professionals'
    ];
    
    for (let i = 0; i < testDescriptions.length; i++) {
        const description = testDescriptions[i];
        console.log(`${i + 1}️⃣ Testing: "${description}"`);
        
        try {
            const response = await fetch('http://localhost:3000/api/generate-names', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json'
                },
                body: JSON.stringify({
                    description: description
                })
            });
            
            const result = await response.json();
            
            if (result.success) {
                console.log(`✅ SUCCESS - Generated by: ${result.source}`);
                console.log(`📝 Message: ${result.message}`);
                console.log('🎯 Generated Names:');
                result.names.forEach((name, index) => {
                    console.log(`   ${index + 1}. ${name.name} - ${name.description}`);
                });
            } else {
                console.log('❌ FAILED:', result.error || result.message);
            }
        } catch (error) {
            console.log('❌ ERROR:', error.message);
        }
        
        console.log('\n' + '='.repeat(60) + '\n');
        
        // Wait a bit between requests to avoid rate limiting
        await new Promise(resolve => setTimeout(resolve, 2000));
    }
    
    console.log('🎉 Business Name Generation Test Complete!');
}

// Run the test
testBusinessNameGeneration().catch(console.error);
